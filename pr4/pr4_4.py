def solve_eq():
    #  –±–µ–∑–ø–æ—Å–µ—Ä–µ–¥–Ω—î —Å—É—Å—ñ–¥—Å—Ç–≤–æ –¥–≤–æ—Ö –¥—ñ–π—Å–Ω–∏—Ö —á–∏—Å–µ–ª
    l = 1.6
    r = 3
    m = (l + r) / 2.0   
    while l != m and m != r:
        if m/3 - sin(m) < 0:
            l = m
        else:
            r = m
        m = (l + r) / 2.0
    return l


