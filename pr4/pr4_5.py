def foo(x):
    return x ** 3 + 4 * x ** 2 + x - 6


def solve_eq(f, a, b, c):
    #  –±–µ–∑–ø–æ—Å–µ—Ä–µ–¥–Ω—î —Å—É—Å—ñ–¥—Å—Ç–≤–æ –¥–≤–æ—Ö –¥—ñ–π—Å–Ω–∏—Ö —á–∏—Å–µ–ª
    l = a
    r = b
    m = (l + r) / 2.0
    while l != m and m != r:
        if f(m) < c:
            l = m
        else:
            r = m
        m = (l + r) / 2.0
    return l


